#!/usr/bin/env node
/**
 * Compiles the Tailwind utility classes used by the installed streamdown dist
 * into src/components/MarkdownRenderer/streamdown-tailwind.generated.css,
 * with Streamdown's shadcn-style tokens bridged to Ant Design CSS variables
 * (see streamdown-theme.css for the bridge and rationale).
 *
 * The output is stamped with a hash of the inputs (streamdown dist files +
 * the theme bridge). MarkdownRenderer.generated-css.test.ts recomputes the
 * hash so CI fails when a streamdown upgrade or patch forgets to regenerate.
 */
import { execFileSync } from 'node:child_process';
import { createHash } from 'node:crypto';
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { createRequire } from 'node:module';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const require = createRequire(import.meta.url);
const uiRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');

export const THEME_CSS_PATH = path.join(
  uiRoot,
  'src/components/MarkdownRenderer/streamdown-theme.css'
);
export const GENERATED_CSS_PATH = path.join(
  uiRoot,
  'src/components/MarkdownRenderer/streamdown-tailwind.generated.css'
);
export const HASH_STAMP_PATTERN = /source-hash: ([0-9a-f]{64}) \(streamdown@([^)]+)\)/;

export function resolveStreamdownPackage() {
  // streamdown's exports map is ESM-only; styles.css is the one subpath
  // resolvable from CJS require.resolve.
  const packageRoot = path.dirname(require.resolve('streamdown/styles.css'));
  const packageJson = JSON.parse(readFileSync(path.join(packageRoot, 'package.json'), 'utf8'));
  return { distDir: path.join(packageRoot, 'dist'), version: packageJson.version };
}

/** Hash of everything the generated CSS is derived from. */
export function computeSourceHash() {
  const { distDir } = resolveStreamdownPackage();
  const hash = createHash('sha256');
  hash.update(readFileSync(THEME_CSS_PATH));
  for (const file of readdirSync(distDir).sort()) {
    if (!file.endsWith('.js')) continue;
    hash.update(file);
    hash.update(readFileSync(path.join(distDir, file)));
  }
  return hash.digest('hex');
}

function main() {
  execFileSync('tailwindcss', ['-i', THEME_CSS_PATH, '-o', GENERATED_CSS_PATH], {
    cwd: uiRoot,
    stdio: 'inherit',
  });
  const { version } = resolveStreamdownPackage();
  const stamp = `/* @generated by \`pnpm gen:streamdown-css\` — do not edit. source-hash: ${computeSourceHash()} (streamdown@${version}) */\n`;
  writeFileSync(GENERATED_CSS_PATH, stamp + readFileSync(GENERATED_CSS_PATH, 'utf8'));
}

if (process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  main();
}
